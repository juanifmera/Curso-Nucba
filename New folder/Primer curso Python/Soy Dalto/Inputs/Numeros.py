#Pidiendo numeros
number = input("Che campeon, decime un numero que te lo multiplico por lo que quieras ")
multiplicador = input("Ahora decime por cuanto queres que te lo multiplique, ciruja " )

#Convirtiendo numeros
resultado = int(number) * int(multiplicador)

print(f"Aca tenes tu cuentita de mierda = {resultado}")